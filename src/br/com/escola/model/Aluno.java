package br.com.escola.model;

public class Aluno {

	// modelo
	private String nome;
	private String rg;
	private String cpf;
	private String endereco;
	private String pai;
	private String mae;
	private String emailPai;
	private String emailMae;
	private String telPai;
	private String telMae;
	private String idade;

	// getters e setters
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getRg() {
		return rg;
	}

	public void setRg(String rg) {
		this.rg = rg;
	}

	public String getCpf() {
		return cpf;
	}

	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

	public String getEndereco() {
		return endereco;
	}

	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}

	public String getPai() {
		return pai;
	}

	public void setPai(String pai) {
		this.pai = pai;
	}

	public String getMae() {
		return mae;
	}

	public void setMae(String mae) {
		this.mae = mae;
	}

	public String getEmailPai() {
		return emailPai;
	}

	public void setEmailPai(String emailPai) {
		this.emailPai = emailPai;
	}

	public String getEmailMae() {
		return emailMae;
	}

	public void setEmailMae(String emailMae) {
		this.emailMae = emailMae;
	}

	public String getTelPai() {
		return telPai;
	}

	public void setTelPai(String telPai) {
		this.telPai = telPai;
	}

	public String getTelMae() {
		return telMae;
	}

	public void setTelMae(String telMae) {
		this.telMae = telMae;
	}

	public String getIdade() {
		return idade;
	}

	public void setIdade(String idade) {
		this.idade = idade;
	}
	
}
